package com.example.cognify;

public class learning_tech {
    String tech_name;
    String tech_detail;

    public learning_tech(String tech_name, String tech_detail) {
        this.tech_name = tech_name;
        this.tech_detail = tech_detail;
    }

    public String getTech_name() {
        return tech_name;
    }

    public String getTech_detail() {
        return tech_detail;
    }
}
